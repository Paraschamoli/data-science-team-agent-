def relocate_imports_inside_function(  # noqa: C901 - complex import handling is intentional
    function_code: str,
) -> str:
    """Move imports inside function to avoid global namespace issues."""
    lines = function_code.split("\n")
    imports = []
    function_body = []
    inside_function = False
    function_indent = 0

    for line in lines:
        stripped = line.strip()

        # Detect function definition
        if stripped.startswith("def ") and ":" in stripped:
            inside_function = True
            function_indent = len(line) - len(line.lstrip())
            function_body.append(line)
            continue

        if inside_function:
            # Check if we're still in the function
            current_indent = len(line) - len(line.lstrip())
            if line.strip() and current_indent <= function_indent and not stripped.startswith("#"):
                # End of function
                inside_function = False

            # Collect imports inside function
            if inside_function and (stripped.startswith("import ") or stripped.startswith("from ")):
                imports.append(stripped)
            else:
                function_body.append(line)
        else:
            # Collect imports outside function
            if stripped.startswith("import ") or stripped.startswith("from "):
                imports.append(stripped)
            else:
                function_body.append(line)

