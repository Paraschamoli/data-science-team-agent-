def add_comments_to_top(code: str, agent_name: str) -> str:
    """Add agent name and timestamp comments to code."""
    import datetime

    timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    comment = f"""
# Generated by {agent_name}
# Timestamp: {timestamp}
# AI Data Science Team

"""
    return comment + code


def format_agent_name(agent_name: str) -> str:
    """Format agent name for display."""
    return f"[{agent_name.upper()}]"


def format_recommended_steps(steps: str, heading: str = "# Recommended Steps:") -> str:
    """Format recommended steps with heading."""
    return f"{heading}\n\n{steps}"


def get_generic_summary(data: dict[str, Any]) -> str:
    """Generate a generic summary from data."""
    if not data:
        return "No data available."

    summary_lines = ["## Summary"]

    for key, value in data.items():
        if isinstance(value, dict):
            summary_lines.append(f"### {key.title()}")
            for sub_key, sub_value in value.items():
                summary_lines.append(f"- {sub_key}: {sub_value}")
        else:
            summary_lines.append(f"- {key}: {value}")

    return "\n".join(summary_lines)


def remove_consecutive_duplicates(items: list) -> list:
    """Remove consecutive duplicates from list."""
    if not items:
        return []

    result = [items[0]]
    for item in items[1:]:
        if item != result[-1]:
            result.append(item)

    return result


def clean_code_output(code: str) -> str:
    """Clean code output by removing extra whitespace and formatting."""
    # Remove multiple consecutive newlines
    code = re.sub(r"\n\s*\n\s*\n", "\n\n", code)

    # Remove trailing whitespace
    code = "\n".join(line.rstrip() for line in code.split("\n"))

    return code.strip()
